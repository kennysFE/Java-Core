package AlgorithmJava;

public class SwapTwoNumber {
	public static void main(String[] args) {
		// Ex1: Swap two number not using variable thrild
		int a = 10;
		int b = 20;
		a = a + b; // a = 30
		b = a - b; // b = 10
		a = a - b; // a = 20

		System.out.println(" a = " + a + " b = " + b);

	}
}
